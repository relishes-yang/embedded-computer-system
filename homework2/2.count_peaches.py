"""
2.（练习实例80）海滩上有一堆桃子，五只猴子来分。第一只猴子把这堆桃子平均分为五份，多了一个，这只猴子把多的一个扔入海中，拿走了一份。
第二只猴子把剩下的桃子又平均分成五份，又多了一个，它同样把多的一个扔入海中，拿走了一份，
第三、第四、第五只猴子都是这样做的，问海滩上原来最少有多少个桃子？
"""""


def find_min_peaches_optimized():
    """
    优化版：从后往前推，提高计算效率
    """
    # 第五只猴子分之前有x个桃子
    # x = 5a + 1，剩下4a
    # 第四只猴子分之前有y个桃子
    # y = 5b + 1，剩下4b = x
    # 所以y = (5/4)x + 1

    # 从x开始，x必须满足：x % 5 == 1 且 x % 4 == 0
    x = 6  # 从第一个满足x % 5 == 1的数开始
    while True:
        if x % 5 == 1 and x % 4 == 0:
            # 计算第四只猴子分之前的桃子数
            y = (5 * x) // 4 + 1
            if y % 5 == 1 and y % 4 == 0:
                # 计算第三只猴子分之前的桃子数
                z = (5 * y) // 4 + 1
                if z % 5 == 1 and z % 4 == 0:
                    # 计算第二只猴子分之前的桃子数
                    w = (5 * z) // 4 + 1
                    if w % 5 == 1 and w % 4 == 0:
                        # 计算第一只猴子分之前的桃子数
                        v = (5 * w) // 4 + 1
                        if v % 5 == 1 and v % 4 == 0:
                            return v
        x += 5  # 每次增加5，保持x % 5 == 1

# 找到最小的初始桃子数
min_peaches = find_min_peaches_optimized()
print(f"海滩上原来最少有 {min_peaches} 个桃子")